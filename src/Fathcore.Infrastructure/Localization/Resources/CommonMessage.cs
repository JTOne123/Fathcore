namespace Fathcore.Infrastructure.Localization.Resources
{
    /// <summary>
    /// Represents common message
    /// </summary>
    public sealed class CommonMessage
    {
        /// <summary>
        /// The default value used for hello message.
        /// <para>
        /// Message: Hello, the Web API is ready to rock!
        /// </para>
        /// </summary>
        public const string Hello = "Hello";

        /// <summary>
        /// The default value used for success message.
        /// <para>
        /// Message: Request successful.
        /// </para>
        /// </summary>
        public const string Success = "Success";

        /// <summary>
        /// The default value used for exception message.
        /// <para>
        /// Message: Request responded with exceptions.
        /// </para>
        /// </summary>
        public const string Exception = "Exception";

        /// <summary>
        /// The default value used for unauthorized message.
        /// <para>
        /// Message: Request denied.
        /// </para>
        /// </summary>
        public const string Unauthorized = "Unauthorized";

        /// <summary>
        /// The default value used for validation error message.
        /// <para>
        /// Message: Request responded with validation error(s).
        /// </para>
        /// </summary>
        public const string ValidationError = "ValidationError";

        /// <summary>
        /// The default value used for validation detail message.
        /// <para>
        /// Message: Please correct the specified validation errors and try again.
        /// </para>
        /// </summary>
        public const string ValidationDetail = "ValidationDetail";

        /// <summary>
        /// The default value used for failure message.
        /// <para>
        /// Message: Unable to process the request.
        /// </para>
        /// </summary>
        public const string Failure = "Failure";

        /// <summary>
        /// The default value used for not found message.
        /// <para>
        /// Message: The specified URI does not exist. Please verify and try again.
        /// </para>
        /// </summary>
        public const string UriNotFound = "UriNotFound";

        /// <summary>
        /// The default value used for contact support message.
        /// <para>
        /// Message: Your request cannot be processed. Please contact a support.
        /// </para>
        /// </summary>
        public const string ContactSupport = "ContactSupport";

        /// <summary>
        /// The default value used for unhandled message.
        /// <para>
        /// Message: An unhandled error occurred.
        /// </para>
        /// </summary>
        public const string Unhandled = "Unhandled";

        /// <summary>
        /// The default value used for conflict message.
        /// <para>
        /// Message: The data may have been modified or deleted since it were loaded.
        /// </para>
        /// </summary>
        public const string Conflict = "Conflict";
    }
}
